public class Position implements Cloneable {
    private float x;
    private float y;

    Position(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    @Override
    public Position clone() {
        return new Position(x,y);
    }
}
